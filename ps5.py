def read_trigger_config(filename):
    """
    filename: the name of a trigger configuration file

    Returns: a list of trigger objects specified by the trigger configuration
        file.
    """
    # We give you the code to read in the file and eliminate blank lines and
    # comments. You don't need to know how it works for now!
    trigger_file = open(filename, 'r')
    lines = []
    for line in trigger_file:
        line = line.rstrip()
        if not (len(line) == 0 or line.startswith('//')):
            lines.append(line)

    triggers_list = []
    triggers_dict = {}
    final_list = []
    for line in lines:
        triggers_list.append(line.split(","))

    for i in range(len(triggers_list)):
        if triggers_list[i][1] == "TITLE":
            triggers_dict[triggers_list[i][0]] = TitleTrigger(triggers_list[i][2])
        if triggers_list[i][1] == "DESCRIPTION":
            triggers_dict[triggers_list[i][0]] = DescriptionTrigger(triggers_list[i][2])
        if triggers_list[i][1] == "AFTER":
            triggers_dict[triggers_list[i][0]] = AfterTrigger(triggers_list[i][2])
        if triggers_list[i][1] == "BEFORE":
            triggers_dict[triggers_list[i][0]] = BeforeTrigger(triggers_list[i][2])
        if triggers_list[i][1] == "AND":
            triggers_dict[triggers_list[i][0]] = AndTrigger(triggers_dict[triggers_list[i][2]], triggers_dict[triggers_list[i][3]])
        if triggers_list[i][1] == "NOT":
            triggers_dict[triggers_list[i][0]] = NotTrigger(triggers_list[i][2])
        if triggers_list[i][0] == "ADD":
            for j in range(1, len(triggers_list[i])):
                final_list.append(triggers_dict[triggers_list[i][j]])
    return final_list

